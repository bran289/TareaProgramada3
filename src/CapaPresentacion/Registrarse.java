    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(this.radioManager.isSelected()){
            try{
            Conexion cone = new Conexion();
            Connection con = cone.Conexion();
            String query = "insert into login(usuario, rol, Contrasenia) values(?,'Manager',PWDENCRYPT(?))";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setString(1, usuario.getText());
            pst.setString(2, contra.getText());
            pst.executeUpdate();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else if(this.radioAdministrator.isSelected()){
            try{
                Conexion cone = new Conexion();
                Connection con = cone.Conexion();
                String query = "insert into login(usuario, rol, Contrasenia) values(?,'Administrator',PWDENCRYPT(?))";
                PreparedStatement pst = con.prepareStatement(query);
                pst.setString(1, usuario.getText());
                pst.setString(2, contra.getText());
                pst.executeUpdate();
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
        else if(this.radioAdministrator.isSelected()){
            JOptionPane.showMessageDialog(this, "Tiene que seleccionar una opci√≥n de rol", "Advertencia",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

